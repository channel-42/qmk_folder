bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  if (record->event.pressed) {
#ifdef OLED_DRIVER_ENABLE
    set_keylog(keycode, record);
    set_progressbar('>', '-');
#endif
    // set_timelog();
  }
  return true;
}
